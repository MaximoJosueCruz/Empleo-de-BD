    public boolean VerChar(int key){
        mayusculas = key >= 65 && key <= 90;
        minusculas = key >= 97 && key <= 122;
        espacio = key == 32;
            
        if (!(minusculas || mayusculas || espacio)){
            return false;
       }
         return true;
    }
    
    public boolean VerNum(int key){
        numeros = key >= 48 && key <= 57;
            
        if (!numeros){
            return false;
       }
         return true;
    }
    
