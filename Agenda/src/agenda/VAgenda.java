    public void verTabla(String id,String NombB,String catB){
        modeloD= new DefaultTableModel();
        //Titulo de las columnas
        modeloD.addColumn("Id");
        modeloD.addColumn("Nombre");
        modeloD.addColumn("Apellido_P");
        modeloD.addColumn("Apellido_M");
        modeloD.addColumn("Direccion");
        modeloD.addColumn("Celular");
        modeloD.addColumn("Correo");
        modeloD.addColumn("Categoria");
        tblContactos.setModel(modeloD);
        //Arreglo para llenar la tabla
        
        sql= "";
        
        //con esto  dependiendo del número de tarjeta de busque, se va a mostrar información, si está vacía mostrará todo
        if(NombB.equals("") && id.equals("") && catB.equals("")){
            sql="SELECT * FROM contactos";
        }else{
            sql="SELECT * FROM contactos WHERE ";  //Nombre='"+NombB+"'";
            if(!NombB.equals("") && id.equals("") && catB.equals(".")){
                sql= sql+" Nombre='"+NombB+"'";
            }
            if(NombB.equals("") && !id.equals("") && catB.equals(".")){
                sql= sql+" Id='"+id+"'";
            }
            if(NombB.equals("") && id.equals("") && !catB.equals(".")){
                sql= sql+" Categoria='"+catB+"'";
            }
            if(!NombB.equals("") && !id.equals("") && catB.equals(".")){
                sql= sql+" Nombre='"+NombB+"' AND Id='"+id+"'";
            }
            if(NombB.equals("") && !id.equals("") && !catB.equals(".")){
                sql= sql+" Id='"+id+"' AND Categoria='"+catB+"'";
            }
            if(!NombB.equals("") && id.equals("") && !catB.equals(".")){
                sql= sql+" Nombre='"+NombB+"' AND Categoria='"+catB+"'";
            }
            if(!NombB.equals("") && !id.equals("") && !catB.equals(".")){
                sql= sql+" Nombre='"+NombB+"' AND Id='"+id+"' AND Categoria='"+catB+"'";
            }
            /*if(NombB.equals("")){
                sql=sql+" Id='"+id+"'";
            }else if(id.equals("")){
                sql=sql+" Nombre='"+NombB+"'";
            }else{
                sql=sql+" Id='"+id+"' AND Nombre='"+NombB+"'";
            }*/
        }
        //fin de búsqueda
        
        try{
            sentencia= reg.createStatement();
            rs= sentencia.executeQuery(sql);
            while(rs.next()){
                datos[0]=rs.getString(1);//campos de la tabla de la bd, inicia desde 1
                datos[1]=rs.getString(2);
                datos[2]=rs.getString(3);
                datos[3]=rs.getString(4);
                datos[4]=rs.getString(5);
                datos[5]=rs.getString(6);
                datos[6]=rs.getString(7);
                datos[7]=rs.getString(8);
                modeloD.addRow(datos);
            }
            tblContactos.setModel(modeloD);
        }catch(SQLException ex){
            Logger.getLogger(VAgenda.class.getName()).log(Level.SEVERE,null,ex);
        }
        
    }
    
    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if(txtNombre.getText().equals("")||txtApellidoP.getText().equals("")||txtApellidoM.getText().equals("")||txtDireccion.getText().equals("")||txtCelular.getText().equals("")||txtCorreo.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Por favor, llene todos los campos para poder guardar al contacto");
        }else{
            nombre=txtNombre.getText();
            apellidoP=txtApellidoP.getText();
            apellidoM=txtApellidoM.getText();
            direccion=txtDireccion.getText();
            celular=txtCelular.getText();
            correo=txtCorreo.getText();
            categoria=comboCat.getSelectedItem().toString();
            sql= "INSERT INTO contactos (nombre,apellido_P,apellido_M,direccion,celular,correo,categoria)VALUES (?,?,?,?,?,?,?)";
            
            try{
                ps= reg.prepareStatement(sql);
                ps.setString(1,nombre);
                ps.setString(2,apellidoP);
                ps.setString(3,apellidoM);
                ps.setString(4,direccion);
                ps.setString(5,celular);
                ps.setString(6,correo);
                ps.setString(7,categoria);
                ps.executeUpdate();
                verTabla("","","");
                JOptionPane.showMessageDialog(null, "Contacto registrado :D");
                limpiar();
            }catch(SQLException ex){
                Logger.getLogger(VAgenda.class.getName()).log(Level.SEVERE,null,ex);
            }
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

